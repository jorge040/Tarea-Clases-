public class CLASE_UNO {

    private String Dos;
    private int Tres;
    private double Cuatro;
    private int Cinco;

    private static String Uno;
    public CLASE_UNO(String uno, String dos, int tres, double cuatro, int cinco) {
        this.Uno = uno;
        this.Dos = dos;
        this.Tres = tres;
        this.Cuatro = cuatro;
        this.Cinco = cinco;

    }

    public static String getUno(){
        return Uno;
    }

    public void setUno(){
        this.Uno = Uno;
    }

    public String getDos(){
        return Dos;
    }
    public void setDos(){
        this.Dos = Dos;
    }
    public int getTres(){
        return Tres;
    }
    public void setTres(){
        this.Tres = Tres;
    }
    public double getCuatro(){
        return Cuatro;
    }
    public void setCutro(){
        this.Cuatro = Cuatro;
    }
    public int getCinco(){
        return Cinco;
    }
    public void setCinco(){
        this.Cinco = Cinco;
    }

    

    
    


    
}
